

function Freq_limit = Aster_range_freq_limit(Range_num)

switch Range_num

    case 1
        Freq_limit = 200; % Hz

    case 2
        Freq_limit = 200; % Hz

    case 3
        Freq_limit = 200; % Hz

    case 4
        Freq_limit = 70; % Hz

    case 5
        Freq_limit = 2; % Hz

    case 6
        Freq_limit = 0.2; % Hz

end

end